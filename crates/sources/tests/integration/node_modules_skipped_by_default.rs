//! Default ignore rules must skip node_modules/ vendor trees.

use crate::support::split_chunk_results;
use keyhog_core::Source;
use keyhog_sources::FilesystemSource;

#[test]
fn node_modules_skipped_by_default() {
    let dir = tempfile::tempdir().expect("tempdir");
    let nm = dir.path().join("node_modules").join("pkg");
    std::fs::create_dir_all(&nm).expect("mkdir");
    std::fs::write(
        nm.join("secret.js"),
        "TOKEN=hidden-in-node-modules
",
    )
    .expect("write");
    std::fs::write(
        dir.path().join("app.env"),
        "TOKEN=visible-root
",
    )
    .expect("write");

    let source = FilesystemSource::new(dir.path().to_path_buf());
    let rows: Vec<_> = source.chunks().collect();
    let (chunks, errors) = split_chunk_results(&rows);
    assert!(
        errors.is_empty(),
        "default node_modules exclude fixture must not emit SourceError rows, got {errors:?}"
    );
    assert_eq!(
        chunks.len(),
        1,
        "only the root file should be scanned; got {chunks:?}"
    );
    assert_eq!(chunks[0].metadata.source_type.as_ref(), "filesystem");
    assert!(
        chunks[0]
            .metadata
            .path
            .as_deref()
            .is_some_and(|path| path.ends_with("app.env")),
        "surviving chunk must carry root path metadata, got {:?}",
        chunks[0].metadata.path
    );
    let bodies: Vec<String> = chunks.iter().map(|c| c.data.to_string()).collect();
    assert!(bodies.iter().any(|b| b.contains("visible-root")));
    assert!(
        !bodies.iter().any(|b| b.contains("hidden-in-node-modules")),
        "node_modules must be excluded"
    );
}
